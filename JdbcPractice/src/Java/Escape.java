package Java;

public class Escape {
	public static void main(String[] args) {
		System.out.println("Hello Good\tMorning");
		
		System.out.println("Hello Good\b Morning");
		
		System.out.println("Hello Good\nMorning");
		
		System.out.println("Hello \'Good\' Morning");
		
		System.out.println("Hello \"Good\"Morning");
		
		System.out.println("Hello \\Good\\ Morning");
	}

}
