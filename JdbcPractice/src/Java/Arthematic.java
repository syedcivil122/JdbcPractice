package Java;

public class Arthematic {
 public static void main(String[] args) {
	int a = 20, b = 30;
	
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	
	System.out.println();
	double x =5, y=7;
	System.out.println(x+y);
	System.out.println(x-y);
	System.out.println(x*y);
	System.out.println(x/y);
	System.out.println(x%y);
	
	
}
}
